num_a = 9
num_b = 5

# Addition
print(f'Addition:\t {num_a} + {num_b} = {num_a + num_b}')

# Substraction
print(f'Substraction:\t {num_a} - {num_b} = {num_a - num_b}')

# Division 
print(f'Division:\t {num_a} / {num_b} = {num_a / num_b}')

# Multiplication
print(f'Multiplication:\t {num_a} * {num_b} = {num_a * num_b}')

# Exponent
print(f'Exponent:\t {num_a} ** {num_b} = {num_a ** num_b}') 

# Modulus
print(f'Modulus:\t {num_a} % {num_b} = {num_a % num_b}')

# Floor division
print(f'Floor divison:\t {num_a} // {num_b} = {num_a // num_b}')